<h1>Home</h1>
<p>
Welcome! OTA Update Center is an Over The Air update service, that is free of use. This project is especially
for the rom devs around, to be able to implement an easy to use, and free OTA Update app. The project 
has one goal, to provide a way to provide OTA Updates as easy and noob friendly as possible.
</p>

<h2>How does it work?</h2>
<p>
   We provide a server backend and an updater app. Devs just have to register, make an entry of their 
ROM, add a few lines on their build.prop, provide a direct-link to the download and optionally a changelog. 
The rest is done by our service. Users will automatically be informed for any updates and installation is automated
<h2>Features</h2>

    <ul><li>Add and remove OTA Updates in a centralised database.</li>
	<li>Check for updates on boot of devices.</li>
	<li>Check for updates manually when the person wants to.</li>
	<li>Download the rom and flash it directly.</li>
	<li>Download the rom without flashing.</li>
	<li>Wipe data and or cache/dalvik before flashing the update.</li>
	<li>Its free to use!</li>
</ul>
